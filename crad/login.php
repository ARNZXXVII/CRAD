    $username = $_POST['username'];
    $password = $_POST['password'];

    // Check if username and password are correct
    $stmt = $pdo->prepare("SELECT id, access FROM crad_users WHERE username = :username AND password = :password");
    $stmt->bindParam(':username', $username);
    $stmt->bindParam(':password', $password);
    $stmt->execute();

    $user = $stmt->fetch(PDO::FETCH_ASSOC);

    if ($user) {
      // Set session variables
      $_SESSION['id'] = $user['id'];
      $_SESSION['access'] = $user['access'];

      // Redirect user based on user type
      if ($_SESSION['access'] == 'Admin') {
        header("Location: ../crad/index.php");
      } else if ($_SESSION['access'] == 'Coordinator') {
        header("Location: ../crad/coordinator.php");
      } else if ($_SESSION['access'] == 'Staff') {
        header("Location: ../crad/staff.php");
      }
